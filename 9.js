let x = 10;

x += 5;     // x = 10 + 5 = 15  Add and assign
console.log(x);  // 15

x -= 3;     // x = 15 - 3 = 12
console.log(x);  // 12

x *= 2;     // x = 12 * 2 = 24
console.log(x);  // 24

x /= 4;     // x = 24 / 4 = 6
console.log(x);  // 6

x %= 4;     // x = 6 % 4 = 2   Modulus and assign
console.log(x);  // 2
